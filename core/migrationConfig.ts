/**
 * サブコレクション移行の設定
 * 
 * Phase 1-2: サブコレクションと配列形式の両方に保存（後方互換性）
 * Phase 3: 既存データの移行
 * Phase 4: 配列形式への保存を停止（移行完了後）
 * Phase 5: 配列形式の削除（完全移行後）
 */

/**
 * 配列形式への保存を停止するかどうか
 * true: サブコレクションのみに保存（配列形式には保存しない）
 * false: サブコレクションと配列形式の両方に保存（デフォルト）
 * 
 * 注意: 移行が完了し、すべてのユーザーでサブコレクションからデータが読み込めることを確認してから true に設定してください
 */
export const SKIP_ARRAY_HISTORY_SAVE = true;

/**
 * 配列形式からの読み込みを停止するかどうか
 * true: サブコレクションのみから読み込む（配列形式は読み込まない）
 * false: サブコレクション優先、なければ配列形式から読み込む（デフォルト）
 * 
 * 注意: 移行が完了し、すべてのユーザーでサブコレクションにデータがあることを確認してから true に設定してください
 */
export const SKIP_ARRAY_HISTORY_READ = false;
